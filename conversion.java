package javaprogram;

import java.util.Scanner;

public class conversion {
	public static void main(String [] args)
	{
		Scanner bb=new Scanner(System.in);
		byte a=bb.nextByte();
		long h=bb.nextLong();
		int c=a;
		long d=a;
		
		int g=(int)h;
		byte k=(byte)h;
		System.out.println(c);
		System.out.println(d);
		System.out.println(g);
		System.out.println(k);
		
	
		
	}

}
