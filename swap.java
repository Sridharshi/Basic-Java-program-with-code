package javaprogram;

import java.util.*;

public class swap {
	
	public static void main(String [] args) {
		Scanner oo=new Scanner(System.in);
		int a=oo.nextInt();
		int b=oo.nextInt();
		System.out.println("a=" +a);
		System.out.println("b=" +b);
		
		int temp = a;
		int c = b;
		int d = temp;
		System.out.println("After swapping");
		System.out.println("a=" +c);
		System.out.println("b=" +d);

				
				
	}

}
